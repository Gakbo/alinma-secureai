"""
Pluggable SMS dataset registry.
=================================

The goal: add a new SMS dataset by writing ONE small config entry here,
without touching any training script (train_tfidf_baseline.py,
train_bert_finetune.py, etc.). Every training script imports
`load_all_sms_data()` from this module and receives a single, clean,
normalized DataFrame with exactly two columns:

    message : str    the raw SMS text
    label   : str    normalized to "safe" or "phishing"

Every dataset -- whatever its original columns or label encoding -- is
mapped into that common shape by its config. This is the "adapter"
pattern: each dataset gets a small adapter; the training code only ever
sees the normalized output.

--------------------------------------------------------------------------
HOW TO ADD A NEW DATASET (the whole point):
--------------------------------------------------------------------------
1. Put the CSV in the data/ folder.
2. Add one SmsDatasetConfig entry to the DATASETS list below, describing:
     - where the file is
     - which column holds the message text
     - which column holds the label
     - how that dataset's label values map to "safe"/"phishing"
3. Done. Re-run any training script; the new data is included automatically.

No training-code changes. No merging logic to edit. That is the design.
"""
import os
from dataclasses import dataclass, field
from typing import Callable, Optional

import pandas as pd

# Resolve data/ relative to the ml-training project root (one level up from
# this sms_model/ folder), so the registry works whether you run a training
# script from the project root OR run this module directly from sms_model/.
_PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DATA_DIR = os.path.join(_PROJECT_ROOT, "data")


@dataclass
class SmsDatasetConfig:
    """Adapter describing how to read ONE dataset into the common format."""
    name: str                       # human-readable, for logging
    filename: str                   # file inside data/
    message_col: str                # column name holding the SMS text
    label_col: str                  # column name holding the label
    # Maps this dataset's raw label values -> "safe" | "phishing".
    # Keys can be strings or ints; we normalize both.
    label_map: dict
    encoding: str = "utf-8"         # some datasets need "latin-1"
    # Optional extra column renames applied BEFORE selecting message/label,
    # useful for messy files (e.g. Kaggle's v1/v2).
    pre_rename: dict = field(default_factory=dict)
    # Optional custom loader if a CSV is weird (rare). If set, it takes the
    # full path and returns a DataFrame with message_col & label_col present.
    custom_reader: Optional[Callable[[str], pd.DataFrame]] = None

    # ---- Metadata & weighting (upgrade requirements #6, #7) ----
    # weight: per-row sample weight passed to training. Real/academic data
    # should outweigh synthetic. 1.0 = neutral.
    weight: float = 1.0
    # priority: on cross-dataset duplicate messages, the higher-priority
    # dataset's row is kept (higher number wins).
    priority: int = 50
    source: str = ""                # provenance / citation
    language: str = ""              # "ar", "en", "mixed"
    license: str = ""               # dataset license if known
    description: str = ""


