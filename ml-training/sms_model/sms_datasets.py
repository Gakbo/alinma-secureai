def _normalize_label_map(label_map: dict) -> dict:
    """Allow configs to use ints or strings; match against stringified keys."""
    norm = {}
    for k, v in label_map.items():
        norm[str(k).strip().lower()] = v
    return norm


def _load_one(cfg: SmsDatasetConfig) -> Optional[pd.DataFrame]:
    path = os.path.join(DATA_DIR, cfg.filename)
    if not os.path.exists(path):
        print(f"[skip] {cfg.name}: file not found ({path})")
        return None

    try:
        if cfg.custom_reader:
            df = cfg.custom_reader(path)
        else:
            df = pd.read_csv(path, encoding=cfg.encoding)
    except Exception as e:
        print(f"[warn] {cfg.name}: could not read ({e}) -- skipping.")
        return None

    if cfg.pre_rename:
        df = df.rename(columns=cfg.pre_rename)

    if cfg.message_col not in df.columns or cfg.label_col not in df.columns:
        print(f"[warn] {cfg.name}: expected columns "
              f"'{cfg.message_col}' and '{cfg.label_col}' not found. "
              f"Actual columns: {list(df.columns)} -- skipping.")
        return None

    out = df[[cfg.message_col, cfg.label_col]].copy()
    out.columns = ["message", "label"]

    # Normalize labels through the config's map.
    norm_map = _normalize_label_map(cfg.label_map)
    out["label"] = out["label"].map(lambda x: norm_map.get(str(x).strip().lower()))

    before = len(out)
    out = out.dropna(subset=["message", "label"])
    out = out[out["label"].isin(["safe", "phishing"])]
    out["message"] = out["message"].astype(str).str.strip()
    dropped = before - len(out)

    # Attach per-row metadata for weighting & priority-aware dedup.
    out["sample_weight"] = cfg.weight
    out["dataset_priority"] = cfg.priority
    out["dataset_source"] = cfg.name

    print(f"[ok]   {cfg.name}: {len(out)} rows (weight={cfg.weight}, "
          f"priority={cfg.priority})"
          + (f" ({dropped} dropped as unmapped/empty)" if dropped else ""))
    return out


def load_all_sms_data(verbose: bool = True) -> pd.DataFrame:
    """
    Load and merge EVERY registered dataset that is present on disk.
    Returns a deduplicated DataFrame with columns [message, label],
    label in {"safe", "phishing"}.

    Training scripts call ONLY this function.
    """
    frames = []
    for cfg in DATASETS:
        df = _load_one(cfg)
        if df is not None and len(df) > 0:
            frames.append(df)

    if not frames:
        raise FileNotFoundError(
            "No SMS datasets found in data/. Generate the synthetic set with "
            "generate_arabic_sms_samples.py, and/or download the CSVs listed "
            "in the DATASETS registry."
        )

    merged = pd.concat(frames, ignore_index=True)
    # Priority-aware dedup: when the same message appears in multiple
    # datasets, keep the row from the highest-priority source.
    merged = (merged.sort_values("dataset_priority", ascending=False)
                    .drop_duplicates(subset="message")
                    .reset_index(drop=True))

    if verbose:
        print(f"\n[total] {len(merged)} unique SMS rows after merge + "
              f"priority-aware dedup")
        print(merged["label"].value_counts().to_string())
        print()
    return merged


def _inspect(path: str):
    """Utility: print a dataset's columns + first rows so you can fill in a config."""
    for enc in ("utf-8", "latin-1"):
        try:
            df = pd.read_csv(path, encoding=enc, nrows=5)
            print(f"Read with encoding={enc}")
            print("Columns:", list(df.columns))
            print(df.head().to_string())
            return
        except Exception as e:
            print(f"[{enc}] failed: {e}")
    print("Could not read the file with utf-8 or latin-1.")


if __name__ == "__main__":
    import sys
    if len(sys.argv) == 3 and sys.argv[1] == "--inspect":
        _inspect(sys.argv[2])
    else:
        # Running the module directly loads everything and prints a summary.
        load_all_sms_data()
